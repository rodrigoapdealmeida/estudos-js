// Não podemos criar constantes com palavras reservadas (Pela linguagem Javascript)
// Exemplo: const const, const if, const for.
// Constantes precisam de nomes significativos.
// Não pode começar o nome de umaconstantes com um número.
// Não podem conter espaços ou traços.
// Utilizamos camelCase
// Exemplo: const nomeCompletoDoCliente;
// Case-sensitive
// Exemplo: const nomeCliente; , const nomecliente; são diferentes.
// Não podemos redeclarar constantes com const, apenas modificar o valor.
// Não UTILIZE VAR UTILIZE CONST

// Jvascript é uma liguagem de tipagem dinamica
// String = Texto | Number = Número
const nome = 'João';    // Constantes precisam ser inicializada com algum valor e não podem ser modificadas.
console.log('nome');
console.log(typeof nome); // typeof exibe a informação do tipo de dado atribuido a variável, neste caso, uma string.

